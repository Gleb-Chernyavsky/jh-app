import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IAmountOfGoods } from '@/shared/model/amount-of-goods.model';

const baseApiUrl = 'api/amount-of-goods';

export default class AmountOfGoodsService {
  public find(id: number): Promise<IAmountOfGoods> {
    return new Promise<IAmountOfGoods>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IAmountOfGoods): Promise<IAmountOfGoods> {
    return new Promise<IAmountOfGoods>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IAmountOfGoods): Promise<IAmountOfGoods> {
    return new Promise<IAmountOfGoods>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
